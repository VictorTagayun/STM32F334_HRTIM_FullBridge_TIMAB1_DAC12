/**
  * @brief This function handles HRTIM timer A global interrupt.
  */
void HRTIM1_TIMA_IRQHandler(void)
{
  /* USER CODE BEGIN HRTIM1_TIMA_IRQn 0 */

	//set high
	GPIOB->BSRR = 0x00000100;

	/* Clear Interrupt flag */
	__HAL_HRTIM_TIMER_CLEAR_IT(&hhrtim1, HRTIM_TIMERINDEX_TIMER_A, HRTIM_TIM_IT_REP);

  /* USER CODE END HRTIM1_TIMA_IRQn 0 */
  /* USER CODE BEGIN HRTIM1_TIMA_IRQn 1 */

	/* Compute PI Controller */
//	CurrentDutyA = PI_Controller();
	CurrentDutyA = my_PID_Controller();
	HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, VoutConversion);

	/* Update Duty1A on BUCK side */
	__HAL_HRTIM_SETCOMPARE(&hhrtim1, HRTIM_TIMERINDEX_TIMER_A, HRTIM_COMPAREUNIT_1, CurrentDutyA);
	__HAL_HRTIM_SETCOMPARE(&hhrtim1, HRTIM_TIMERINDEX_TIMER_B, HRTIM_COMPAREUNIT_1, CurrentDutyA+23040);

	// set low
	GPIOB->BSRR = 0x01000000;


  /* USER CODE END HRTIM1_TIMA_IRQn 1 */
}

