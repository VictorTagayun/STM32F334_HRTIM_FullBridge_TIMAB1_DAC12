int32_t my_PID_Controller(void)
{

  VoutConversion = HAL_ADCEx_InjectedGetValue(&hadc1, ADC_INJECTED_RANK_1);

  last_error = error;
  error = (int32_t) VoutT - (int32_t ) VoutConversion;
  errorSum = errorSum + error * 5;
  errorRate = (error - last_error)/2;

  if (errorSum > SAT_LIMIT)
  {
	  errorSum = SAT_LIMIT;
  }
  if (errorSum < -(SAT_LIMIT))
  {
	  errorSum = -(SAT_LIMIT);
  }

  pid_out = Kp * error + errorSum * Ki + Kd * errorRate;

  if (pid_out >= MAX_DUTY)
  {
    pid_out = MAX_DUTY;
  }

  if (pid_out <= MIN_DUTY)
  {
    pid_out = MIN_DUTY;
  }

  return  pid_out;
}

